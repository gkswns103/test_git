package khairline;

public class User {

	private String id;
	private String pw;
	private String name;
	private String airline_ticket;
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPw() {
		return pw;
	}
	public void setPw(String pw) {
		this.pw = pw;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAirline_ticket() {
		return airline_ticket;
	}
	public void setAirline_ticket(String airline_ticket) {
		this.airline_ticket = airline_ticket;
	}
}
